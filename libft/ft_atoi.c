#include "libft.h"

int		ft_atoi(const char *str)
{
	unsigned int	i;
	int				neg;
	int				var;

	var = 0;
	neg = 0;
	i = 0;
	while (((str[i] >= 8) && (str[i] <= 13)) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
		var = var * 10 + str[i++] - '0';
	if (neg == 1)
		return (-var);
	return (var);
}
