#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list *list;
	t_list *list_next;

	list = *alst;
	while (list != NULL)
	{
		del(list->content, list->content_size);
		list_next = list->next;
		free(list);
		list = list_next;
	}
	*alst = NULL;
}
