#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t		i;
	char		*idest;
	const char	*isrc;

	i = 0;
	idest = dest;
	isrc = src;
	while (i < n)
	{
		idest[i] = isrc[i];
		i++;
	}
	return (idest);
}
